# алфавит 13сс
s = "0123456789abc"
# в алфавите 15сс в расчетах будет
# использоваться цифра не более "c"
for A in range(1, 100_000):
    for x in s:
        for y in s:
            M = "2" + y + "23" + x + "5"
            N = "67" + x + "9" + y
            M = int(M,15)
            N = int(N,13)
            if  (M + A) % N == 0:
                print(A)
                exit()
            

