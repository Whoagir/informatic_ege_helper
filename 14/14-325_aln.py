x = 11*15**65 + 18*15**38 - 14*15**17 + 19*15**11 + 18338 
m = set()
s = ''
while (x != 0):
    s = str(x % 15)
    if s == '10': s = 'A'
    if s == '11': s = 'B'
    if s == '12': s = 'C'
    if s == '13': s = 'D'
    if s == '14': s = 'E'
    
    m.add(s)
    x = x // 15
  
print('n = ',len(m),';','  m = ', m )
