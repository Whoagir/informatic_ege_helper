x = 12**34 + 7*(12**26) - 3*(12**16) + 2*(12**5) + 552 

m = set()
s = ''
while (x != 0):
    s = str(x % 12)
    if s == '10': s = 'A'
    if s == '11': s = 'B'
    m.add(s)
    x = x // 12
  
print('n = ',len(m),';','  m = ', m )
