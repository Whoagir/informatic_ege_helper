# Решение методом динамического программирования
n=100
f=[0, 2, 4, 6, 8, 10] # массив f для i <= 5
for i in range( 6, n+1 ):    
    if i%2 == 0:
        f.append( f[i-2] + 3 * f[i//2] + i )
    else:
        f.append( f[i-1] + f[i-2] + f[i-3] )
print(f) # отладочная
print( f[n-1] + f[n] )
