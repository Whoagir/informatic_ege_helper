'''
F(0) = 1
F(n) = F(n–1), при 0 < n ≤ 10
F(n) = 2,2*F(n–3), при 10 < n <100
F(n) = 1,7*F(n–2), при n ≥ 100
Чему равна целая часть значения функции F(22)?
'''
def F( n ):
  if n == 0: return 1
  elif 0<n <= 10: return F(n-1)
  elif 10<n <= 100: return 2.2*F(n-3)
  
  else:
      if n >= 100:
        return 1.7* F(n-2) 
  

print( int(F(22)))
           

'''
23
'''
